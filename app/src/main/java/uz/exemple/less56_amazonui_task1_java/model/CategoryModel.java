package uz.exemple.less56_amazonui_task1_java.model;

public class CategoryModel {
    private int image;
    private String title;

    public CategoryModel(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
